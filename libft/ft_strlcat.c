#include "libft.h"

size_t	ft_strlcat(char *dest, const char *src, size_t size)
{
	size_t	i;
	size_t	j;
	size_t	dest_len;

	i = 0;
	while (dest[i] != '\0' && i < size)
	{
		i++;
	}
	dest_len = i;
	if (i == size)
		return (dest_len + ft_strlen(src));
	j = 0;
	while (src[j] != '\0' && i < size - 1)
	{
		dest[i] = src[j];
		i++;
		j++;
	}
	dest[i] = '\0';
	return (dest_len + ft_strlen(src));
}
